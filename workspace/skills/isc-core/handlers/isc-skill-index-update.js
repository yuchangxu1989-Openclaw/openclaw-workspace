#!/usr/bin/env node
/**
 * ISC Handler: Skill Index Auto-Update
 * Scans all skills and regenerates the CAPABILITY-ANCHOR.md index.
 * Used by: rule.isc-skill-index-auto-update-001
 */
'use strict';

const fs = require('fs');
const path = require('path');
const { scanFiles, checkFileExists, writeReport, gateResult } = require('../lib/handler-utils');

const WORKSPACE = process.env.WORKSPACE || path.resolve(__dirname, '../../..');
const SKILLS_DIR = path.join(WORKSPACE, 'skills');
const OUTPUT_FILE = path.join(WORKSPACE, 'CAPABILITY-ANCHOR.md');

/**
 * Extract skill metadata from SKILL.md
 */
function extractSkillMeta(skillDir) {
  const skillMdPath = path.join(skillDir, 'SKILL.md');
  if (!checkFileExists(skillMdPath)) return null;

  const content = fs.readFileSync(skillMdPath, 'utf8');
  const name = path.basename(skillDir);

  // Extract first heading as title
  const titleMatch = content.match(/^#\s+(.+)/m);
  const title = titleMatch ? titleMatch[1].trim() : name;

  // Extract description: first non-empty, non-heading line
  const lines = content.split('\n');
  let description = '';
  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed && !trimmed.startsWith('#')) {
      description = trimmed;
      break;
    }
  }

  // Count handlers, rules, scripts
  const handlersDir = path.join(skillDir, 'handlers');
  const rulesDir = path.join(skillDir, 'rules');
  let handlerCount = 0;
  let ruleCount = 0;

  if (checkFileExists(handlersDir)) {
    try { handlerCount = fs.readdirSync(handlersDir).filter(f => f.endsWith('.js')).length; } catch {}
  }
  if (checkFileExists(rulesDir)) {
    try { ruleCount = fs.readdirSync(rulesDir).filter(f => f.endsWith('.json')).length; } catch {}
  }

  return { name, title, description, handlerCount, ruleCount };
}

function generateIndex(skills) {
  const lines = [
    '# CAPABILITY-ANCHOR.md',
    '',
    `> Auto-generated by isc-skill-index-update handler at ${new Date().toISOString()}`,
    '',
    `Total skills: ${skills.length}`,
    '',
    '| Skill | Description | Handlers | Rules |',
    '|-------|-------------|----------|-------|',
  ];

  for (const s of skills.sort((a, b) => a.name.localeCompare(b.name))) {
    const desc = s.description.length > 80 ? s.description.slice(0, 77) + '...' : s.description;
    lines.push(`| ${s.name} | ${desc} | ${s.handlerCount} | ${s.ruleCount} |`);
  }

  lines.push('');
  return lines.join('\n');
}

function main() {
  const checks = [];

  if (!checkFileExists(SKILLS_DIR)) {
    checks.push({ name: 'skills-dir', ok: false, message: `Skills directory not found: ${SKILLS_DIR}` });
    const result = gateResult('isc-skill-index-update', checks);
    console.log(JSON.stringify(result, null, 2));
    process.exit(result.exitCode);
    return;
  }

  const skills = [];
  let dirs;
  try {
    dirs = fs.readdirSync(SKILLS_DIR).filter(d =>
      fs.statSync(path.join(SKILLS_DIR, d)).isDirectory()
    );
  } catch (e) {
    checks.push({ name: 'read-skills', ok: false, message: e.message });
    const result = gateResult('isc-skill-index-update', checks);
    console.log(JSON.stringify(result, null, 2));
    process.exit(result.exitCode);
    return;
  }

  for (const dir of dirs) {
    const meta = extractSkillMeta(path.join(SKILLS_DIR, dir));
    if (meta) skills.push(meta);
  }

  checks.push({ name: 'scan-skills', ok: true, message: `Found ${skills.length} skills with SKILL.md` });

  // Generate and write index
  const indexContent = generateIndex(skills);
  try {
    fs.writeFileSync(OUTPUT_FILE, indexContent, 'utf8');
    checks.push({ name: 'write-index', ok: true, message: `Updated ${OUTPUT_FILE}` });
  } catch (e) {
    checks.push({ name: 'write-index', ok: false, message: e.message });
  }

  const reportPath = path.join(WORKSPACE, 'reports', 'skill-index-update-latest.json');
  const result = gateResult('isc-skill-index-update', checks);
  writeReport(reportPath, result);

  console.log(JSON.stringify(result, null, 2));
  process.exit(result.exitCode);
}

main();
