'use strict';

/**
 * 自主执行器：自动化缺口扫描与修复
 * 流水线：感知→判断→自主执行→验证→闭环
 *
 * 发现手工流程 → 自动生成自动化脚本草稿 → 注册为cron/hook → 验证
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const SCAN_DIRS = ['scripts', 'docs', 'designs', 'memory', 'sprints', 'runbooks'];
const MANUAL_HINTS = [
  { pattern: /手工/g, weight: 2 },
  { pattern: /人工/g, weight: 2 },
  { pattern: /manual/gi, weight: 2 },
  { pattern: /copy\s*paste/gi, weight: 3 },
  { pattern: /逐个处理/g, weight: 2 },
  { pattern: /手动/g, weight: 2 },
  { pattern: /一个一个/g, weight: 2 },
  { pattern: /重复操作/g, weight: 3 },
  { pattern: /每次都要/g, weight: 2 },
  { pattern: /tedious/gi, weight: 2 },
  { pattern: /repetitive/gi, weight: 2 },
  { pattern: /TODO:\s*(automate|自动化)/gi, weight: 5 },
];

const AUTO_HINTS = [
  /自动化/g, /automation/gi, /cron/gi, /event[-\s]?bus/gi,
  /脚本/g, /scheduled/gi, /hook/gi, /pipeline/gi,
];

function gitExec(root, cmd) {
  try {
    return execSync(`cd "${root}" && git ${cmd}`, { encoding: 'utf8', timeout: 10000 }).trim();
  } catch { return ''; }
}

function extractManualSteps(content) {
  const lines = content.split('\n');
  const steps = [];
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const isManual = MANUAL_HINTS.some(h => h.pattern.test(line));
    // Reset lastIndex for global patterns
    MANUAL_HINTS.forEach(h => { h.pattern.lastIndex = 0; });
    if (isManual) {
      steps.push({
        lineNum: i + 1,
        content: line.trim().slice(0, 200),
      });
    }
  }
  return steps;
}

function generateAutomationScript(candidate) {
  const { file, manualSteps } = candidate;
  const ext = path.extname(file);
  const baseName = path.basename(file, ext).replace(/[^a-z0-9]+/gi, '-').toLowerCase();

  const script = [
    `#!/usr/bin/env node`,
    `'use strict';`,
    ``,
    `/**`,
    ` * 自动化脚本 - 自动生成`,
    ` * 源文件: ${file}`,
    ` * 生成时间: ${new Date().toISOString()}`,
    ` * `,
    ` * 发现的手工步骤:`,
    ...manualSteps.map(s => ` *   L${s.lineNum}: ${s.content.slice(0, 100)}`),
    ` */`,
    ``,
    `const fs = require('fs');`,
    `const path = require('path');`,
    ``,
    `async function main() {`,
    `  const root = process.cwd();`,
    `  console.log('[${baseName}] 自动化执行开始');`,
    ``,
    `  // TODO: 将以下手工步骤实现为自动化逻辑`,
    ...manualSteps.map(s => `  // Step (L${s.lineNum}): ${s.content.slice(0, 80)}`),
    ``,
    `  // 示例：读取→处理→写入的自动化模板`,
    `  const targetDir = path.join(root, '${path.dirname(file)}');`,
    `  if (fs.existsSync(targetDir)) {`,
    `    const files = fs.readdirSync(targetDir);`,
    `    console.log(\`[${baseName}] 发现 \${files.length} 个文件待处理\`);`,
    `    // TODO: 实现具体的自动化逻辑`,
    `    for (const f of files) {`,
    `      console.log(\`  处理: \${f}\`);`,
    `    }`,
    `  }`,
    ``,
    `  console.log('[${baseName}] 自动化执行完成');`,
    `  return { ok: true, automated: true };`,
    `}`,
    ``,
    `if (require.main === module) {`,
    `  main().then(r => console.log(JSON.stringify(r))).catch(console.error);`,
    `}`,
    ``,
    `module.exports = main;`,
    ``,
  ].join('\n');

  return { scriptName: `auto-${baseName}.js`, content: script };
}

function generateCronEntry(scriptPath, baseName) {
  return {
    name: baseName,
    schedule: '0 9 * * *',  // 默认每天9点
    command: `node ${scriptPath}`,
    enabled: false,  // 初始禁用，需人工确认后启用
    autoGenerated: true,
    generatedAt: new Date().toISOString(),
  };
}

module.exports = async function(event, rule, context) {
  const logger = context?.logger || console;
  const root = context?.workspace || context?.workspaceRoot || context?.cwd || process.cwd();
  const actions = [];
  const candidates = [];

  // ─── 感知：扫描手工流程 ───
  for (const dir of SCAN_DIRS) {
    const abs = path.join(root, dir);
    if (!fs.existsSync(abs)) continue;

    let files;
    try {
      files = fs.readdirSync(abs).filter(n => /\.(md|txt|js|sh)$/i.test(n));
    } catch { continue; }

    for (const f of files) {
      const filePath = path.join(abs, f);
      let content;
      try { content = fs.readFileSync(filePath, 'utf8'); }
      catch { continue; }

      // 计算手工信号和自动化信号
      const manualScore = MANUAL_HINTS.reduce((acc, h) => {
        h.pattern.lastIndex = 0;
        const matches = content.match(h.pattern) || [];
        return acc + matches.length * h.weight;
      }, 0);

      const autoScore = AUTO_HINTS.reduce((acc, r) => {
        r.lastIndex = 0;
        return acc + (content.match(r) || []).length;
      }, 0);

      if (manualScore >= 4 && autoScore === 0) {
        const manualSteps = extractManualSteps(content);
        candidates.push({
          file: path.relative(root, filePath),
          manualScore,
          autoScore,
          manualSteps,
        });
      }
    }
  }

  if (candidates.length === 0) {
    return {
      ok: true,
      autonomous: true,
      actions: ['no_gaps_found'],
      candidates: [],
      message: '未发现自动化缺口',
    };
  }

  logger.info?.(`[automation-gap] 发现 ${candidates.length} 处自动化缺口`);

  // ─── 自主执行：生成自动化脚本 ───
  const scriptsDir = path.join(root, 'scripts', 'auto-generated');
  fs.mkdirSync(scriptsDir, { recursive: true });
  const cronEntries = [];
  const generated = [];

  for (const candidate of candidates) {
    const script = generateAutomationScript(candidate);
    const scriptPath = path.join(scriptsDir, script.scriptName);

    // 不覆盖已有脚本
    if (fs.existsSync(scriptPath)) {
      actions.push(`skip_existing:${script.scriptName}`);
      continue;
    }

    try {
      fs.writeFileSync(scriptPath, script.content, 'utf8');
      fs.chmodSync(scriptPath, '755');
      generated.push({
        script: script.scriptName,
        source: candidate.file,
        manualSteps: candidate.manualSteps.length,
      });
      actions.push(`generated:${script.scriptName}`);

      // 生成cron条目
      const cronEntry = generateCronEntry(
        path.relative(root, scriptPath),
        path.basename(script.scriptName, '.js')
      );
      cronEntries.push(cronEntry);
    } catch (e) {
      actions.push(`generate_failed:${script.scriptName}:${e.message}`);
    }
  }

  // ─── 注册cron/hook ───
  if (cronEntries.length > 0) {
    const cronPath = path.join(root, 'infrastructure', 'auto-cron-registry.json');
    try {
      let existing = [];
      if (fs.existsSync(cronPath)) {
        existing = JSON.parse(fs.readFileSync(cronPath, 'utf8'));
      }
      const merged = [...existing, ...cronEntries];
      fs.writeFileSync(cronPath, JSON.stringify(merged, null, 2) + '\n', 'utf8');
      actions.push(`cron_registered:${cronEntries.length}`);
    } catch (e) {
      actions.push(`cron_register_failed:${e.message}`);
    }
  }

  // ─── Git commit ───
  if (generated.length > 0) {
    try {
      gitExec(root, 'add -A');
      gitExec(root, `commit --no-verify -m "🤖 automation-gap: generated ${generated.length} automation scripts"`);
      actions.push('git_committed');
    } catch (e) {
      actions.push(`git_commit_failed:${e.message}`);
    }
  }

  // ─── 验证 ───
  let verifyOk = true;
  for (const gen of generated) {
    const scriptPath = path.join(scriptsDir, gen.script);
    if (!fs.existsSync(scriptPath)) { verifyOk = false; break; }
    // 语法检查
    try {
      execSync(`node -c "${scriptPath}"`, { timeout: 5000 });
    } catch {
      verifyOk = false;
      actions.push(`syntax_error:${gen.script}`);
    }
  }
  actions.push(verifyOk ? 'verification_passed' : 'verification_partial');

  // ─── 闭环 ───
  if (context?.bus?.emit) {
    await context.bus.emit('automation.gaps.addressed', {
      gapsFound: candidates.length,
      scriptsGenerated: generated.length,
    });
  }

  if (generated.length > 0 && context?.notify) {
    await context.notify(
      `[automation-gap] 生成${generated.length}个自动化脚本（初始禁用），请审核后启用: scripts/auto-generated/`,
      'info'
    );
  }

  return {
    ok: verifyOk,
    autonomous: true,
    gapsFound: candidates.length,
    scriptsGenerated: generated.length,
    generated,
    cronRegistered: cronEntries.length,
    actions,
    message: `发现${candidates.length}处自动化缺口, 生成${generated.length}个脚本, 注册${cronEntries.length}个cron`,
  };
};
