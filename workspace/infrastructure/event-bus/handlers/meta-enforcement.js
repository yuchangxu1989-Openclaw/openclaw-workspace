const fs = require('fs');
const path = require('path');
const { exists, readText, readJson, walk } = require('./p0-utils');

/**
 * 元执行力审计：规则→handler存在性+可执行性全面检查
 * 感知：isc.general.created/updated / orchestration.general.review_requested
 * 执行：扫描所有规则→检查handler→不合格自动生成骨架→require验证→闭环
 */
module.exports = async function(event, rule, context) {
  const workspace = (context && context.workspace) || '/root/.openclaw/workspace';
  const logger = context.logger;
  const bus = context.bus;

  logger.info('[meta-enforcement] 启动元执行力审计');

  try {
    const rulesDir = path.join(workspace, 'infrastructure', 'isc', 'rules');
    const handlersDir = path.join(workspace, 'infrastructure', 'event-bus', 'handlers');

    if (!exists(rulesDir)) {
      logger.warn('[meta-enforcement] 规则目录不存在:', rulesDir);
      return { status: 'skipped', reason: 'rules_dir_not_found' };
    }

    const ruleFiles = walk(rulesDir, ['.json']);
    const report = {
      timestamp: new Date().toISOString(),
      totalRules: ruleFiles.length,
      enforced: 0,
      unenforced: 0,
      autoGenerated: 0,
      details: []
    };

    for (const ruleFile of ruleFiles) {
      const entry = {
        rule: path.basename(ruleFile),
        handler: null,
        handlerExists: false,
        handlerLoadable: false,
        autoGenerated: false,
        status: 'unknown'
      };

      try {
        const ruleData = readJson(ruleFile);
        const handlerName = ruleData?.action?.handler;

        if (!handlerName) {
          entry.status = 'no_handler_defined';
          report.unenforced++;
          report.details.push(entry);
          logger.warn(`[meta-enforcement] 规则无handler定义: ${entry.rule}`);
          continue;
        }

        entry.handler = handlerName;
        const handlerPath = path.join(handlersDir, handlerName);

        // 检查handler文件是否存在
        if (exists(handlerPath)) {
          entry.handlerExists = true;

          // 尝试require验证可加载
          try {
            const loaded = require(handlerPath);
            if (typeof loaded === 'function') {
              entry.handlerLoadable = true;
              entry.status = 'enforced';
              report.enforced++;
            } else {
              entry.status = 'handler_not_function';
              report.unenforced++;
              logger.warn(`[meta-enforcement] handler不是函数: ${handlerName}`);
            }
          } catch (loadErr) {
            entry.status = 'handler_load_error';
            entry.loadError = loadErr.message;
            report.unenforced++;
            logger.warn(`[meta-enforcement] handler加载失败: ${handlerName}`, loadErr.message);
          } finally {
            // 清除require缓存避免污染
            delete require.cache[require.resolve(handlerPath)];
          }
        } else {
          // handler不存在，自动生成骨架
          logger.info(`[meta-enforcement] handler不存在，自动生成: ${handlerName}`);
          const skeleton = generateHandlerSkeleton(ruleData, handlerName);
          
          fs.writeFileSync(handlerPath, skeleton, 'utf-8');
          entry.autoGenerated = true;
          report.autoGenerated++;

          // 再次require验证
          try {
            const loaded = require(handlerPath);
            if (typeof loaded === 'function') {
              entry.handlerExists = true;
              entry.handlerLoadable = true;
              entry.status = 'enforced_auto';
              report.enforced++;
              logger.info(`[meta-enforcement] 自动生成handler验证通过: ${handlerName}`);
            } else {
              entry.status = 'auto_gen_not_function';
              report.unenforced++;
            }
          } catch (loadErr) {
            entry.status = 'auto_gen_load_error';
            entry.loadError = loadErr.message;
            report.unenforced++;
            logger.error(`[meta-enforcement] 自动生成handler加载失败: ${handlerName}`, loadErr.message);
          } finally {
            delete require.cache[require.resolve(handlerPath)];
          }
        }
      } catch (parseErr) {
        entry.status = 'rule_parse_error';
        entry.parseError = parseErr.message;
        report.unenforced++;
        logger.warn(`[meta-enforcement] 规则解析失败: ${entry.rule}`, parseErr.message);
      }

      report.details.push(entry);
    }

    // 计算执行力
    report.enforcementRate = report.totalRules > 0
      ? `${Math.round((report.enforced / report.totalRules) * 100)}%`
      : '100%';

    // 写入报告
    const reportsDir = path.join(workspace, 'infrastructure', 'event-bus', 'reports');
    if (!fs.existsSync(reportsDir)) {
      fs.mkdirSync(reportsDir, { recursive: true });
    }
    const reportPath = path.join(reportsDir, `meta-enforcement-${Date.now()}.json`);
    fs.writeFileSync(reportPath, JSON.stringify(report, null, 2), 'utf-8');
    logger.info(`[meta-enforcement] 审计报告: ${reportPath}`);

    logger.info('[meta-enforcement] 审计完成', {
      totalRules: report.totalRules,
      enforced: report.enforced,
      unenforced: report.unenforced,
      autoGenerated: report.autoGenerated,
      rate: report.enforcementRate
    });

    bus.emit('meta.enforcement.audit.completed', {
      totalRules: report.totalRules,
      enforced: report.enforced,
      unenforced: report.unenforced,
      autoGenerated: report.autoGenerated,
      enforcementRate: report.enforcementRate,
      reportPath
    });

    return {
      status: 'completed',
      totalRules: report.totalRules,
      enforced: report.enforced,
      unenforced: report.unenforced,
      autoGenerated: report.autoGenerated,
      enforcementRate: report.enforcementRate,
      reportPath
    };
  } catch (err) {
    logger.error('[meta-enforcement] 执行失败:', err.message);
    bus.emit('meta.enforcement.audit.failed', { error: err.message });
    throw err;
  }
};

function generateHandlerSkeleton(ruleData, handlerName) {
  const ruleName = ruleData.name || handlerName.replace('.js', '');
  const description = ruleData.description || '自动生成的handler骨架';
  const events = ruleData.trigger?.events || [];

  return `const fs = require('fs');
const path = require('path');
const { exists, readText } = require('./p0-utils');

/**
 * ${description}
 * 规则: ${ruleData.id || ruleName}
 * 感知: ${events.join(', ') || 'N/A'}
 * 
 * ⚠️ 此handler由meta-enforcement自动生成骨架，需要填充具体逻辑
 */
module.exports = async function(event, rule, context) {
  const workspace = (context && context.workspace) || '/root/.openclaw/workspace';
  const logger = context.logger;
  const bus = context.bus;

  logger.info('[${ruleName}] handler执行 (auto-generated skeleton)');

  try {
    // TODO: 填充具体执行逻辑
    const result = {
      status: 'skeleton',
      handler: '${handlerName}',
      rule: '${ruleData.id || ruleName}',
      message: '此handler为自动生成骨架，需要填充具体逻辑',
      event: event.type || event.name || 'unknown'
    };

    logger.warn('[${ruleName}] 使用骨架handler，需要实现具体逻辑');
    bus.emit('${ruleName}.executed', result);

    return result;
  } catch (err) {
    logger.error('[${ruleName}] 执行失败:', err.message);
    throw err;
  }
};
`;
}
