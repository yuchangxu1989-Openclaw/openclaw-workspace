#!/bin/bash
# ============================================================
# C2 自动采集 回归测试 v2 (扩展版 - 覆盖全部8种ISC场景)
# ============================================================
# 覆盖 ISC-EVAL-C2-AUTO-HARVEST-001 定义的全部8种场景
# 每种场景至少2条测试case(正例+反例)
# 通过标准: 全部通过
# ============================================================

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PASS=0
FAIL=0
TOTAL=0

test_case() {
    local desc="$1"
    local msg="$2"
    local expected_harvest="$3"

    TOTAL=$((TOTAL+1))
    RESULT=$(echo "$msg" | bash "$SCRIPT_DIR/intent-probe.sh")
    ACTUAL=$(echo "$RESULT" | jq -r '.should_harvest')
    INTENT=$(echo "$RESULT" | jq -r '.intent_type')
    ENGINE=$(echo "$RESULT" | jq -r '.engine // "unknown"')

    if [ "$ACTUAL" = "$expected_harvest" ]; then
        echo "✅ [$TOTAL] $desc → $INTENT (harvest=$ACTUAL) [$ENGINE]"
        PASS=$((PASS+1))
    else
        echo "❌ [$TOTAL] $desc → $INTENT (expected=$expected_harvest actual=$ACTUAL) [$ENGINE]"
        echo "   完整响应: $RESULT"
        FAIL=$((FAIL+1))
    fi
}

echo "=========================================================="
echo " C2 自动采集 全场景回归测试 v2"
echo " 引擎: LLM(v2-llm) / Fallback(v1-keyword-fallback)"
echo " 时间: $(date -Iseconds)"
echo "=========================================================="
echo ""

# ============================================================
# 场景1: 纠偏类 — 用户否定、修正、要求重做
# ============================================================
echo "--- 场景1: 纠偏类 ---"
test_case "纠偏-正例1: 直接指出错误"        "任务刷新有误，产品洞察任务早就好了"    "true"
test_case "纠偏-正例2: 隐含纠偏语气"        "这个结果跟我要的完全不一样，重新来"    "true"
test_case "纠偏-反例: 正常讨论"              "我觉得这个方案还不错"                  "false"
echo ""

# ============================================================
# 场景2: 反复未果类 — 同一问题修了2次以上仍未解决
# ============================================================
echo "--- 场景2: 反复未果类 ---"
test_case "反复未果-正例1: 明确次数"         "这是第三次出这个问题了"                "true"
test_case "反复未果-正例2: 隐含多次"         "怎么又出同样的错，上次不是修过了吗"    "true"
test_case "反复未果-反例: 首次提问"          "这个功能好像有个小bug"                 "false"
echo ""

# ============================================================
# 场景3: 头痛医头类 — 只改症状不改根因
# ============================================================
echo "--- 场景3: 头痛医头类(纠偏/根因) ---"
test_case "头痛医头-正例1: 指出治标不治本"   "你只是把报错压下去了，根本问题没解决"  "true"
test_case "头痛医头-正例2: 要求深层修复"     "别只改表面，找到根因再改"              "true"
test_case "头痛医头-反例: 纯追问根因"        "这个错误的根本原因是什么"              "false"
echo ""

# ============================================================
# 场景4: 连锁跷跷板类 — 修A导致B坏
# ============================================================
echo "--- 场景4: 连锁跷跷板类 ---"
test_case "跷跷板-正例1: 修A坏B"             "你修了登录页面，结果注册页挂了"        "true"
test_case "跷跷板-正例2: 改了又引入新问题"   "这个改动把之前正常的功能搞坏了"        "true"
test_case "跷跷板-反例: 独立新需求"          "帮我再加一个导出按钮"                  "false"
echo ""

# ============================================================
# 场景5: 自主性缺失类 — 该自己发现的问题等用户指出
# ============================================================
echo "--- 场景5: 自主性缺失类 ---"
test_case "自主性缺失-正例1: 直接指出"       "这个不应该我来提醒，你自己应该检查到"  "true"
test_case "自主性缺失-正例2: 隐含自主性"     "为什么每次都要我来发现这种低级错误"    "true"
test_case "自主性缺失-反例: 正常求助"        "帮我检查一下这段代码有没有bug"         "false"
echo ""

# ============================================================
# 场景6: 全局未对齐类 — 局部修复但其他层未同步
# ============================================================
echo "--- 场景6: 全局未对齐类 ---"
test_case "未对齐-正例1: 层间不一致"         "你只改了前端，后端接口还是旧的没同步"  "true"
test_case "未对齐-正例2: 局部改全局没跟"     "文档更新了但配置文件没有跟着改"        "true"
test_case "未对齐-反例: 正常同步请求"        "前后端都帮我更新一下"                  "false"
echo ""

# ============================================================
# 场景7: 交付质量类 — 半成品/残留/格式错误
# ============================================================
echo "--- 场景7: 交付质量类 ---"
test_case "质量-正例1: 半成品"               "这个报告写了一半就交了，不完整"        "true"
test_case "质量-正例2: 格式问题"             "输出格式全乱了，JSON都不合法"          "true"
test_case "质量-反例: 正常格式要求"          "请用markdown格式输出结果"              "false"
echo ""

# ============================================================
# 场景8: 认知错误类 — 对需求理解偏差
# ============================================================
echo "--- 场景8: 认知错误类 ---"
test_case "认知-正例1: 理解偏差"             "我说的不是这个意思，你理解错了"        "true"
test_case "认知-正例2: 需求误解"             "需求完全搞反了，我要的是A你做成了B"    "true"
test_case "认知-反例: 正常澄清"             "我补充一下需求细节"                    "false"
echo ""

# ============================================================
# 补充: 否定类 + 教学类 + 正常类
# ============================================================
echo "--- 补充场景 ---"
test_case "否定-正例: 明确拒绝"              "不要这样做，停下来"                    "true"
test_case "教学-正例: 传授规则"              "你要明白，本质上LLM意图分类才是正道"   "true"
test_case "正常-反例1: 闲聊"                 "今天天气真好"                          "false"
test_case "正常-反例2: 简单指令"             "帮我查一下明天的日程"                  "false"

echo ""
echo "=========================================================="
echo " 结果: $PASS/$TOTAL 通过 ($FAIL 失败)"
echo "=========================================================="

if [ "$FAIL" -gt 0 ]; then
    echo "⚠️  存在失败case，请检查"
    exit 1
else
    echo "✅ 全部通过！"
    exit 0
fi
